    <h1>Inicio</h1>
    <p>Osumi Framework es un pequeño proyecto personal que he ido madurando y mejorando con el tiempo, a medida que experimentaba y aprendía.</p>
    <p>Es un pequeño framework orientado al modelo MVC (Modelo, Vista, Controlador) para la creación de aplicaciones web, tanto pequeñas como grandes.</p>
    <p>También permite la creación de scripts ejecutables desde línea de comandos.</p>
    <p>Como todo programador de PHP, empecé creando aplicaciones que funcionaban a base de scripts que mezclaban código PHP, HTML, accesos a bases de datos...</p>
    <p>De ahí, pasé a utilizar Symfony y fué todo un descubrimiento. Pero no acababa de estar del todo contento, me parecía que era algo gigante cuando solo quería hacer un "Hola mundo".</p>
    <p>Así que inspirandome en él (de hecho sigo utilizando sus librerías para la gestión de URLS), hice la primera versión del OFW.</p>
    <p>Resultó ser un "pet project" que me permitía crear más "pet projects" :)</p>
    <p>La segunda versión fué la que más tiempo utilicé y la primera que publiqué en Github. Pero a medida que iba aprendiendo y metiendo más mejoras veía huecos o fallos.</p>
    <p>La versión 3 fué una primera aproximación, pero quise cambiar tantas cosas de golpe que lo dejé lleno de bugs :P</p>
    <p>En la versión 4 se cambió la estructura del framework, la configuración, gestión de tareas...</p>
    <p>Finalmente la versión 5, la actual, ha supuesto una refactorización masiva al añadir PHPdoc en todas las clases y funciones del Framework, se ha añadido tipado fuerte y una gran cantidad de correcciones de errores que han aparecido al revisar todo el código.</p>
    <p>Si has llegado a leer hasta aquí, muchas gracias por interesarte :D</p>
